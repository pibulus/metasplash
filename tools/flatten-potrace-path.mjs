#!/usr/bin/env node
// Flatten potrace SVGs into a portable mascot-paths.svg (viewBox 0 0 1024 1024).
//
// Usage: node tools/flatten-potrace-path.mjs <outer.svg> <inner.svg> > mascot-paths.svg
//
// outer = solid silhouette trace; inner = eroded trace. body-path = outer+inner
// (even-odd ring = chunky outline); background = outer solid (gradient-filled).
// Eyes are engine-drawn ellipses (tweak coords below to fit the art).
import { readFileSync } from "fs";

const [outerFile, innerFile] = process.argv.slice(2);
if (!outerFile || !innerFile) {
  console.error("usage: flatten-potrace-path.mjs <outer.svg> <inner.svg>");
  process.exit(1);
}

// potrace transform: translate(0,1024) scale(0.1,-0.1) => X'=0.1x, Y'=1024-0.1y
function flatten(file) {
  const svg = readFileSync(file, "utf8");
  const d = svg.match(/d="([^"]*)"/)[1];
  const tx = (x) => Math.round(0.1 * x * 100) / 100;
  const ty = (y) => Math.round((1024 - 0.1 * y) * 100) / 100;
  const t = d.match(/[MmLlHhVvCcSsQqTtAaZz]|-?\d*\.?\d+(?:e-?\d+)?/g);
  let o = [],
    i = 0,
    px = 0,
    py = 0,
    sx = 0,
    sy = 0;
  const n = () => parseFloat(t[i++]);
  const num = () => i < t.length && /^-?[\d.]/.test(t[i]);
  while (i < t.length) {
    const c = t[i++];
    if (c === "M") {
      px = n();
      py = n();
      sx = px;
      sy = py;
      o.push(`M ${tx(px)} ${ty(py)}`);
      while (num()) {
        px = n();
        py = n();
        o.push(`L ${tx(px)} ${ty(py)}`);
      }
    } else if (c === "m") {
      px += n();
      py += n();
      sx = px;
      sy = py;
      o.push(`M ${tx(px)} ${ty(py)}`);
      while (num()) {
        px += n();
        py += n();
        o.push(`L ${tx(px)} ${ty(py)}`);
      }
    } else if (c === "L") {
      while (num()) {
        px = n();
        py = n();
        o.push(`L ${tx(px)} ${ty(py)}`);
      }
    } else if (c === "l") {
      while (num()) {
        px += n();
        py += n();
        o.push(`L ${tx(px)} ${ty(py)}`);
      }
    } else if (c === "C") {
      while (num()) {
        const a = n(),
          b = n(),
          cc = n(),
          d2 = n(),
          e = n(),
          f = n();
        o.push(`C ${tx(a)} ${ty(b)} ${tx(cc)} ${ty(d2)} ${tx(e)} ${ty(f)}`);
        px = e;
        py = f;
      }
    } else if (c === "c") {
      while (num()) {
        const a = px + n(),
          b = py + n(),
          cc = px + n(),
          d2 = py + n(),
          e = px + n(),
          f = py + n();
        o.push(`C ${tx(a)} ${ty(b)} ${tx(cc)} ${ty(d2)} ${tx(e)} ${ty(f)}`);
        px = e;
        py = f;
      }
    } else if (c === "Z" || c === "z") {
      o.push("Z");
      px = sx;
      py = sy;
    }
  }
  return o.join(" ");
}

const outer = flatten(outerFile);
const inner = flatten(innerFile);
const bodyRing = outer + " " + inner;

// Eyes — tweak to fit the art (viewBox 0 0 1024 1024; subject centered ~512,512).
const ell = (cx, cy, rx, ry) =>
  `M ${cx - rx} ${cy} a ${rx} ${ry} 0 1 0 ${rx * 2} 0 a ${rx} ${ry} 0 1 0 ${-rx * 2} 0 Z`;
const EYE_Y = 555,
  EYE_RX = 38,
  EYE_RY = 52,
  EYE_LX = 461,
  EYE_RX_C = 586;
const eyeLeft = ell(EYE_LX, EYE_Y, EYE_RX, EYE_RY);
const eyeRight = ell(EYE_RX_C, EYE_Y, EYE_RX, EYE_RY);

process.stdout.write(`<?xml version="1.0" encoding="UTF-8" standalone="no"?>
<svg xmlns="http://www.w3.org/2000/svg" width="0" height="0" style="display: none;">
  <!-- ===================================================================
       MASCOT PATHS LIBRARY — generated by tools/png-to-mascot.md
       viewBox 0 0 1024 1024.
       - mascot-background: solid silhouette (gradient-filled, behind)
       - mascot-body-path: outer+inner contour, even-odd => chunky black outline
       - eyes: engine-drawn ellipses (separate paths) for tracking + blink
       =================================================================== -->
  <path id="mascot-background" d="${outer}"/>
  <path id="mascot-body-path" fill-rule="evenodd" d="${bodyRing}"/>
  <path id="mascot-eye-left-path" d="${eyeLeft}"/>
  <path id="mascot-eye-right-path" d="${eyeRight}"/>
</svg>
`);
